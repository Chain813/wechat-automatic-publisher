from datetime import datetime
from loguru import logger

from config import BRAND_NAME
from core.shared.llm import call_deepseek_with_retry

def generate_github_article(projects):
    logger.info("DeepSeek 正在创作 GitHub 开源项目推荐长文...")
    
    projects_text = ""
    for idx, p in enumerate(projects, 1):
        projects_text += f"\n【项目{idx}】\n"
        projects_text += f"- 仓库名: {p['repo']}\n"
        projects_text += f"- 主要语言: {p['lang']}\n"
        projects_text += f"- 官方描述: {p['desc']}\n"
        if p.get('readme_excerpt'):
            projects_text += f"- README 详情节选: {p['readme_excerpt']}\n"
        if p.get('image_url'):
            projects_text += f"- 配图链接 (必须在介绍末尾原样输出此占位符): 【GITHUB配图：{p['image_url']}】\n"
        else:
            fallback_keyword = f"{p['repo'].split('/')[-1]} {p['lang']} project architecture"
            projects_text += f"- 配图: 无，请在介绍末尾插入配图占位符：【此处插入配图：{fallback_keyword}】\n"

    today_str = datetime.now().strftime("%Y年%m月%d日")
    
    prompt = (
        f"## 任务\n为微信公众号「{BRAND_NAME}」创作一篇名为《今日 GitHub 最火开源项目盘点》的文章。\n"
        f"**当前真实时间是：{today_str}**。\n\n"
        "## 待介绍项目列表\n"
        f"{projects_text}\n\n"
        "## 核心原则 (智能文章润色系统 V3.0 标准)\n"
        "- **去 AI 痕迹**：禁止使用“首先、其次、综上所述、总之”等机械化过渡词。语言要自然、口语化，像朋友在微信群里给你安利好东西。\n"
        "- **数字排序**：【重要】每个项目的大标题前必须加上数字排序（如：1. 项目名...），表示这是本篇文章推荐的第几个项目。\n"
        "- **通俗易懂**：用大白话解释技术概念，多用生活化的类比。比如把“分布式消息队列”说成“快递中转站”，把“ORM”说成“数据库翻译官”。\n"
        "- **说人话**：假设读者是对技术感兴趣但不一定写代码的人。避免堆砌专业术语，遇到术语要随手解释。\n\n"
        "## 写作要求与排版结构\n"
        "1. **引言**：用 100 字左右引出今天的盘点。语气轻松，像在朋友圈发现了好玩的东西忍不住分享，比如“今天逛 GitHub 又挖到宝了，有几个项目太有意思了，忍不住给大家唠唠...”。\n"
        "2. **项目逐一介绍**：每个项目使用 `## [序号]. 项目名 (主语言)` 作为大标题。内容结构：\n"
        "   - **一句话亮点**：用 `> ` 引用包裹，用最通俗的话概括这个项目“干了件什么事”。\n"
        "   - **配图占位符**：必须原样输出提供的占位符（包含在内容末尾）。\n"
        "   - **它到底能干嘛**：用大白话描述这个项目解决了什么问题。段落前用一个接地气的 ### 标题（示例：`### 这东西其实就是个XXX`、`### 简单来说，它的核心逻辑是...`、`### 它的本质其实是把XXX给自动化了`）。善用类比，但要避免复读机式地重复“说白了”这种词，用词要丰富多样。\n"
        "   - **谁会用到它**：描述什么样的人、什么场景下会受益。段落前用一个描述性的 ### 标题（示例：`### 哪些人应该赶紧收藏`、`### 如果你经常遇到XXX，选它就对了`、`### 适合它的真实场景`）。\n"
        "3. **互动**：结尾随性一点，呼吁大家去点个 Star，语气轻松幽默。\n\n"
        "## 微信排版铁律\n"
        "1. **禁止行首冒号**：绝对不允许在行首单独出现冒号（如 : 或 ： 这种形式）。如果需要解释，请紧跟在文本后面，不要换行放冒号。\n"
        "2. **禁止空列表项**：不允许出现只有列表符号（- 或 *）但后面没有文字的情况。\n"
        "3. **极短段落**：每段不超过 3 句话，段间必须空行。\n"
        "4. **视觉呼吸**：所有标题和引用前后留白。\n"
        "5. **善用 emoji**：用 🚀、🔧、💡、🤯、👀 等增加趣味性和可读性。\n"
        "6. **严禁结构性标签**：标题必须是有具体意义的口语化句子。\n"
        "7. **重点分级标注**（全文红标不超过 5 处）：\n"
        "   - **红色加粗**：用于全文最核心的结论。用 `**{{{{重点}}}}**` 标记。\n"
        "   - **黑色加粗**：用于项目名、关键概念。用 `**加粗**` 标记。\n\n"
        f"文末加上：**关注「{BRAND_NAME}」，每日获取硬核开源情报。**\n\n"
        "## 输出\n"
        "直接输出正文，不要带有系统回复或 Markdown 代码块包裹。"
    )

    article = call_deepseek_with_retry(prompt, system_content=f"你是一个懂技术但说话特别接地气的科技博主。你最擅长的事情就是把复杂的技术用大白话讲明白，让不写代码的人也能听懂、觉得有趣。你的风格像是在茶余饭后给朋友聊天安利好东西——轻松、幽默、有干货但不端着。**注意：你的词汇要丰富多样，严禁在同一篇文章中反复使用‘说白了’、‘其实’、‘简单来说’等口头禅，要根据语境自然切换表达。**你深谙微信公众号排版节奏。")
    if not article:
        logger.error("  GitHub 文稿生成失败")
        return ""
        
    return article
